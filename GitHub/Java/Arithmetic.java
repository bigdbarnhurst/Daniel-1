class Arithmetic{
	public static void main(String[]  args){
		double result = 25 + 68;
		System.out.println(result);

		result = result - 72;
		System.out.println(result);

		result = result * 54;
		System.out.println(result);

		result = result / 78;
		System.out.println(result);

		result = result % 9;
		System.out.println(result);

		int result2;

		result2 = 15;
		result2++;
		System.out.println(result2);

		result2--;
		System.out.println(result2);

		result2 = result2++ / 4;
		System.out.println(result2);

		System.out.println(result);
		System.out.println(result);

		boolean red = false;
		System.out.println(red);
		System.out.println(!red);
	}
}

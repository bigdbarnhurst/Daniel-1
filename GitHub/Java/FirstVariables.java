class FirstVariables{
  public static void main(String[] args){
    String firstname, lastname;
    int month, day, year;
    firstname = "Daniel";
    lastname = "Barnhurst";
    month = 12;
    day = 2;
    year = 1992;
    System.out.print(firstname);
    System.out.print(" ");
    System.out.print(lastname);
    System.out.println(" was born:");
    System.out.print(month);
    System.out.print("/");
    System.out.print(day);
    System.out.print("/");
    System.out.println(year);
    System.out.print("His age is therefore:");
    System.out.println(2013-year);
  }
}